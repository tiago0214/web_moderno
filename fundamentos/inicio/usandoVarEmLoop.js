//vou usar um laço 'for': é uma laço de repetição
//ele tem 3 partes. A 1 é a declaração de uma variavel; a 2 é a condição que ele vai 
//usar para ficar repetindo esse código; a 3 é o incremento.
for (var i=0; i<10 ;i++ ) {
    console.log(i)
}
//hora que o resultado der falso, ele sai do laço e continua executando o programa
//(o 'i')
console.log('i = ',i)